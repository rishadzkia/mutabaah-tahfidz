<?php

namespace App\Filament\Guru\Resources\Gurus;

use App\Filament\Guru\Resources\Gurus\Pages\CreateGuru;
use App\Filament\Guru\Resources\Gurus\Pages\EditGuru;
use App\Filament\Guru\Resources\Gurus\Pages\ListGurus;
use App\Filament\Guru\Resources\Gurus\Schemas\GuruForm;
use App\Filament\Guru\Resources\Gurus\Tables\GurusTable;
use App\Models\Guru;
use BackedEnum;
use Filament\Resources\Resource;
use Filament\Schemas\Schema;
use Filament\Support\Icons\Heroicon;
use Filament\Tables\Table;

class GuruResource extends Resource
{
    protected static ?string $model = Guru::class;
    protected static ?string $modelLabel = 'Data Guru';
    protected static ?string $pluralModelLabel = 'Data Guru';

    protected static string|BackedEnum|null $navigationIcon = Heroicon::OutlinedRectangleStack;

    protected static ?string $recordTitleAttribute = 'mapel_diampu';
    

    public static function form(Schema $schema): Schema
    {
        return GuruForm::configure($schema);
    }

    public static function table(Table $table): Table
    {
        return GurusTable::configure($table);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => ListGurus::route('/'),
            'create' => CreateGuru::route('/create'),
            'edit' => EditGuru::route('/{record}/edit'),
        ];
    }
}
